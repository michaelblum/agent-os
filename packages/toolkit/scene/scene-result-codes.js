const ERROR_CODES = new Set(DESKTOP_WORLD_SCENE_RESULT_ERROR_CODES)

export function normalizeDesktopWorldSceneResultErrorCode(
  value,
  fallback = 'SCENE_PROJECTION_FAILED',
) {
  if (!ERROR_CODES.has(fallback)) {
    throw new TypeError('DesktopWorld scene result fallback code is not canonical.')
  }
  return typeof value === 'string' && ERROR_CODES.has(value) ? value : fallback
}
